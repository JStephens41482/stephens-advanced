// /api/send-signature-request.js
// Creates a signature_requests row for a job (and its linked invoice if any),
// then sends the customer an email + SMS with a tokenized signing URL.
// Used by the "Send for signature" button on the job detail screen.

const { createClient } = require('@supabase/supabase-js')

module.exports = async function handler(req, res) {
  if (req.method !== 'POST') return res.status(405).end()
  const { job_id, channels } = req.body || {}
  if (!job_id) return res.status(400).json({ error: 'job_id required' })

  const supabaseKey = process.env.SUPABASE_SERVICE_KEY
  if (!supabaseKey) return res.status(500).json({ error: 'SUPABASE_SERVICE_KEY not configured' })
  const supabase = createClient('https://motjasdokoxwiodwzyps.supabase.co', supabaseKey)

  try {
    // Pull job + location + most recent invoice
    const { data: job, error: jobErr } = await supabase
      .from('jobs')
      .select('id,job_number,location_id,billing_account_id,scope,completed_at')
      .eq('id', job_id)
      .maybeSingle()
    if (jobErr || !job) return res.status(404).json({ error: 'Job not found' })

    const { data: loc } = await supabase
      .from('locations')
      .select('id,name,address,city,state,zip,contact_name,contact_phone,contact_email,billing_account_id')
      .eq('id', job.location_id)
      .maybeSingle()

    const { data: ba } = loc?.billing_account_id ? await supabase
      .from('billing_accounts').select('name,contact_name,email,phone').eq('id', loc.billing_account_id).maybeSingle() : { data: null }

    const { data: inv } = await supabase
      .from('invoices').select('id,invoice_number,total').eq('job_id', job_id).is('deleted_at', null).order('date', { ascending: false }).limit(1).maybeSingle()

    const email = loc?.contact_email || ba?.email
    const phone = loc?.contact_phone || ba?.phone
    if (!email && !phone) return res.status(400).json({ error: 'No customer email or phone on file for this location' })

    // Create the signature_request row (token auto-generated by DB default)
    const { data: sigReq, error: sigErr } = await supabase
      .from('signature_requests')
      .insert({
        job_id,
        invoice_id: inv?.id || null,
        location_id: loc?.id || null,
        sent_to_email: email || null,
        sent_to_phone: phone || null,
        status: 'sent'
      })
      .select()
      .single()
    if (sigErr) return res.status(500).json({ error: sigErr.message })

    const signUrl = `https://www.stephensadvanced.com/sign?token=${sigReq.token}`
    const customerName = loc?.contact_name || ba?.contact_name || loc?.name || 'Customer'
    const locationName = loc?.name || ba?.name || ''
    const invNum = inv?.invoice_number || ''
    const total = inv?.total ? `$${(+inv.total).toFixed(2)}` : ''

    const wantEmail = !channels || channels.includes('email')
    const wantSms = !channels || channels.includes('sms')

    let emailResult = null
    let smsResult = null

    if (email && wantEmail) {
      try {
        const html = buildSigRequestEmail({ customerName, locationName, invNum, total, signUrl })
        const r = await fetch('https://api.resend.com/emails', {
          method: 'POST',
          headers: {
            'Authorization': `Bearer ${process.env.RESEND_API_KEY}`,
            'Content-Type': 'application/json'
          },
          body: JSON.stringify({
            from: 'Stephens Advanced <jonathan@stephensadvanced.com>',
            to: [email],
            bcc: ['jonathan@stephensadvanced.com'],
            subject: `Please sign — ${invNum ? 'Invoice ' + invNum + ' · ' : ''}Stephens Advanced${locationName ? ' | ' + locationName : ''}`,
            html
          })
        })
        emailResult = await r.json()
      } catch (e) {
        emailResult = { error: e.message }
      }
    }

    if (phone && wantSms) {
      try {
        const body = `Stephens Advanced: Please review and sign for ${invNum ? 'invoice ' + invNum + ' (' + total + ') ' : 'your recent service '}at ${locationName}. Sign here: ${signUrl}`
        const sid = process.env.TWILIO_ACCOUNT_SID
        const token = process.env.TWILIO_AUTH_TOKEN
        const from = process.env.TWILIO_PHONE_NUMBER
        if (sid && token && from) {
          let to = phone.replace(/[\s\-\(\)\.]/g, '')
          if (!to.startsWith('+')) to = '+1' + to.replace(/^1/, '')
          const auth = Buffer.from(sid + ':' + token).toString('base64')
          const params = new URLSearchParams({ To: to, From: from, Body: body })
          const r = await fetch(`https://api.twilio.com/2010-04-01/Accounts/${sid}/Messages.json`, {
            method: 'POST',
            headers: { 'Authorization': 'Basic ' + auth, 'Content-Type': 'application/x-www-form-urlencoded' },
            body: params.toString()
          })
          smsResult = await r.json()
        } else {
          smsResult = { error: 'Twilio not configured' }
        }
      } catch (e) {
        smsResult = { error: e.message }
      }
    }

    return res.json({
      success: true,
      signature_request_id: sigReq.id,
      token: sigReq.token,
      sign_url: signUrl,
      email: emailResult ? { sent: !emailResult.error, to: email, detail: emailResult } : null,
      sms: smsResult ? { sent: !smsResult.error && !smsResult.code, to: phone, detail: smsResult } : null
    })
  } catch (e) {
    console.error('send-signature-request:', e)
    return res.status(500).json({ error: e.message })
  }
}

